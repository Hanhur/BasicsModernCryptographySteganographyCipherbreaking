def create_random_substitution():
    """Создаёт случайную перестановку английского алфавита"""
    alphabet = list(string.ascii_uppercase)
    shuffled = alphabet.copy()
    random.shuffle(shuffled)
    return dict(zip(alphabet, shuffled))

def encrypt_with_substitution(text: str, cipher_map: dict) -> str:
    """Шифрует текст простой заменой"""
    result = []
    for ch in text.upper():
        if ch in cipher_map:
            result.append(cipher_map[ch])
        else:
            result.append(ch)
    return ''.join(result)

