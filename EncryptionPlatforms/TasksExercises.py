def simulate_scenario(scenario_name: str, letters_arrive_in_order: bool, censor_notices: bool, family_notices: bool):
    print(f"\n🔹 СЦЕНАРИЙ: {scenario_name}")
    print(f"   Письма приходят по порядку: {'✅' if letters_arrive_in_order else '❌'}")
    print(f"   Цензор замечает изменения: {'❌' if censor_notices else '✅'}")
    print(f"   Семья замечает изменения: {'✅' if family_notices else '❌'}")
    
    location = "TUNIS"
    encoded = [('T',), ('U',), ('N',), ('I',), ('S',)]
    
    if not letters_arrive_in_order:
        import random
        encoded = random.sample(encoded, len(encoded))
        print("   ⚠️ Письма перемешаны!")
    
    if censor_notices:
        print("   🚫 Цензор перехватил подозрительную серию писем!")
        print("   Результат: СОЛДАТ РАСКРЫТ")
        return False
    
    if not family_notices:
        print("   😕 Семья не обратила внимания на инициалы")
        print("   Результат: СООБЩЕНИЕ НЕ РАСШИФРОВАНО")
        return False
    
    if not letters_arrive_in_order:
        print(f"   Семья получила буквы: {''.join([x[0] for x in encoded])}")
        print(f"   Ожидалось: {location}")
        print("   Результат: СООБЩЕНИЕ ИСКАЖЕНО")
        return False
    
    print(f"   ✅ Семья прочитала: {location}")
    print("   Результат: УСПЕХ")
    return True

# Запускаем сценарии
