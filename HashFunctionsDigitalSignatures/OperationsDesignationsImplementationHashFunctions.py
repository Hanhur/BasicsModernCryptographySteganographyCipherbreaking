# Операции и обозначения для реализации хеш-функций
# -*- coding: utf-8 -*-
"""
Реализация SHA-1 на Python (без NumPy)
Основано на операциях: AND, OR, XOR, NOT, сдвиги, циклические сдвиги, сложение mod 2 ^ 32
"""

# ============================================
# 1. Базовые операции (из вашего текста)
# ============================================

def rotate_left(x, n):
    """
    Циклический сдвиг влево (операция X <<< r или X ↩ r)
    В SHA-1 используется для сдвига A <<< 5 и B <<< 30
    """
    n = n % 32
    return ((x << n) | (x >> (32 - n))) & 0xFFFFFFFF

def mod_add(*args):
    """
    Оператор ⊞ - сложение по модулю 2 ^ 32
    (X + Y) mod 2 ^ 32
    """
    result = 0
    for val in args:
        result = (result + val) & 0xFFFFFFFF
    return result

# ============================================
# 2. Вспомогательные функции для преобразования
# ============================================

def pad_message(message):
    """
    Дополнение сообщения до размера, кратного 512 битам (64 байта)
    По стандарту SHA-1:
    1) Добавляется бит '1' (0x80)
    2) Добавляются нулевые байты
    3) В конце дописывается длина сообщения в битах (64 бита, big-endian)
    """
    # Исходная длина в байтах
    original_length = len(message)
    # Длина в битах (для вставки в конец)
    bit_length = original_length * 8
    
    # Шаг 1: добавляем байт 0x80 (1000 0000)
    message += b'\x80'
    
    # Шаг 2: добавляем нули, чтобы длина стала сравнима с 56 по модулю 64
    # 56 байт = 448 бит (оставляем 8 байт для длины)
    while len(message) % 64 != 56:
        message += b'\x00'
    
    # Шаг 3: добавляем длину в битах (8 байт, big-endian)
    message += bit_length.to_bytes(8, byteorder = 'big')
    
    return message

def bytes_to_words(data):
    """
    Преобразует байты в 32-битные слова (big-endian)
    Используется для разбиения блока на 16 слов по 32 бита
    """
    words = []
    for i in range(0, len(data), 4):
        word = int.from_bytes(data[i:i + 4], byteorder = 'big')
        words.append(word)
    return words

def words_to_hex(words):
    """
    Преобразует список 32-битных слов в шестнадцатеричную строку
    Каждое слово -> 8 символов HEX (с ведущими нулями)
    """
    return ''.join(f'{w:08x}' for w in words)

# ============================================
# 3. Основные функции SHA-1
# ============================================

def sha1(message):
    """
    Вычисление SHA-1 хеша для сообщения (байтовая строка)
    Возвращает 20-байтовый хеш в виде 40-символьной HEX строки
    """
    
    # ============================================
    # 3.1. Начальные константы (IV - инициализирующие векторы)
    # ============================================
    # Это те самые константы (Kt), о которых говорится в тексте
    # Записаны в шестнадцатеричной системе (как в рис. 4.11)
    h0 = 0x67452301
    h1 = 0xEFCDAB89
    h2 = 0x98BADCFE
    h3 = 0x10325476
    h4 = 0xC3D2E1F0
    
    # Константы для каждого раунда (Kt)
    K = [
        0x5A827999,  # Раунды 0-19
        0x6ED9EBA1,  # Раунды 20-39
        0x8F1BBCDC,  # Раунды 40-59
        0xCA62C1D6   # Раунды 60-79
    ]
    
    # ============================================
    # 3.2. Дополнение сообщения
    # ============================================
    padded_msg = pad_message(message)
    
    # ============================================
    # 3.3. Обработка блоков по 512 бит (64 байта)
    # ============================================
    for chunk_index in range(0, len(padded_msg), 64):
        chunk = padded_msg[chunk_index:chunk_index + 64]
        
        # Разбиваем блок на 16 слов по 32 бита (W[0]...W[15])
        w = bytes_to_words(chunk)
        
        # ============================================
        # 3.4. Расширение сообщения (Message Schedule)
        # Создаем 80 слов из 16 начальных
        # ============================================
        # Расширяем до 80 слов (индексы 0-79)
        for i in range(16, 80):
            # W[i] = (W[i - 3] XOR W[i - 8] XOR W[i - 14] XOR W[i - 16]) <<< 1
            word = w[i - 3] ^ w[i - 8] ^ w[i - 14] ^ w[i - 16]
            w.append(rotate_left(word, 1))
        
        # ============================================
        # 3.5. Инициализация переменных для текущего блока
        # ============================================
        a = h0
        b = h1
        c = h2
        d = h3
        e = h4
        
        # ============================================
        # 3.6. Главный цикл (80 раундов)
        # ============================================
        for i in range(80):
            # Выбор нелинейной функции F в зависимости от раунда
            if 0 <= i <= 19:
                # F = (B ∧ C) ∨ (¬B ∧ D)
                # Используем описанные вами операции AND, OR, NOT
                f = (b & c) | ((~b) & d)
                k = K[0]
            elif 20 <= i <= 39:
                # F = B ⊕ C ⊕ D (XOR)
                f = b ^ c ^ d
                k = K[1]
            elif 40 <= i <= 59:
                # F = (B ∧ C) ∨ (B ∧ D) ∨ (C ∧ D)
                f = (b & c) | (b & d) | (c & d)
                k = K[2]
            else:  # 60-79
                # F = B ⊕ C ⊕ D
                f = b ^ c ^ d
                k = K[3]
            
            # ============================================
            # 3.7. Один раунд SHA-1
            # Используется оператор ⊞ (mod_add) и циклический сдвиг (rotate_left)
            # ============================================
            temp = mod_add(
                rotate_left(a, 5),
                f,
                e,
                k,
                w[i]
            )
            
            # Обновляем регистры (сдвиг вправо)
            e = d
            d = c
            c = rotate_left(b, 30)  # B <<< 30 (циклический сдвиг)
            b = a
            a = temp
            
            # Маскируем, чтобы оставаться в 32 битах
            a &= 0xFFFFFFFF
            b &= 0xFFFFFFFF
            c &= 0xFFFFFFFF
            d &= 0xFFFFFFFF
            e &= 0xFFFFFFFF
        
        # ============================================
        # 3.8. Добавляем результат блока к общему хешу
        # Используется ⊞ (mod_add)
        # ============================================
        h0 = mod_add(h0, a)
        h1 = mod_add(h1, b)
        h2 = mod_add(h2, c)
        h3 = mod_add(h3, d)
        h4 = mod_add(h4, e)
    
    # ============================================
    # 3.9. Формируем финальный хеш (160 бит = 40 HEX символов)
    # ============================================
    return words_to_hex([h0, h1, h2, h3, h4])


