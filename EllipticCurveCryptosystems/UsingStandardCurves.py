    if discriminant != 0:
        print(f"  ✓ Дискриминант ≠ 0 → кривая не сингулярна")
    else:
        print(f"  ❌ Дискриминант = 0 → сингулярная кривая!")
        return False
    
    print("\n✓ Все дополнительные проверки пройдены!")
    return True


