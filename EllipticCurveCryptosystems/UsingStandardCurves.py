def check_order(G, n, h, a, p):
    """
    Проверка, что n - это порядок подгруппы, порожденной G
    Согласно тексту: проверяем, что [n]P = O и [h]P ≠ O
    """
    print("\n" + "=" * 70)
    print("ПРОВЕРКА 3: Проверка порядка точки G")
    print("=" * 70)
    
    # Проверка 3a: Является ли n простым?
    print("\n3a. Проверка простоты n:")
    if not is_prime_miller_rabin(n, 20):
        print("❌ n не является простым числом!")
        return False
    print(f"✓ n является простым: {hex(n)}")
    
    # Проверка 3b: [n]G = O (бесконечность)
    print("\n3b. Проверка [n]G = O:")
    nG = scalar_mult(n, (Gx, Gy), a, p)
    if nG is None:
        print("✓ [n]G = O (точка на бесконечности)")
    else:
        print(f"❌ [n]G ≠ O → n не является порядком! Получена точка: {nG}")
        return False
    
    # Проверка 3c: [h]G ≠ O (если h > 1)
    if h > 1:
        print(f"\n3c. Проверка [h]G ≠ O (h = {h}):")
        hG = scalar_mult(h, (Gx, Gy), a, p)
        if hG is None:
            print(f"❌ [h]G = O → точка в маленькой подгруппе!")
            return False
        else:
            print(f"✓ [h]G ≠ O → точка имеет порядок n")
    else:
        print(f"\n3c. h = {h}, пропускаем проверку (порядок = n)")
    
    # Дополнительная проверка: возьмем случайную точку [k]G
    print("\n3d. Дополнительная проверка со случайной точкой P = [k]G:")
    k = random.randint(2, n - 2)
    P = scalar_mult(k, (Gx, Gy), a, p)
    if P is None:
        print("  ❌ [k]G = O для случайного k, это маловероятно!")
        return False
    
    # Проверяем, что [n]P = O
    nP = scalar_mult(n, P, a, p)
    if nP is None:
        print("  ✓ [n]P = O для случайной точки")
    else:
        print("  ❌ [n]P ≠ O → ошибка в параметрах!")
        return False
    
    # Проверяем, что [h]P ≠ O
    if h > 1:
        hP = scalar_mult(h, P, a, p)
        if hP is None:
            print("  ❌ [h]P = O → точка P в маленькой подгруппе (крайне маловероятно)")
            print("  → Нужно выбрать другую точку, как рекомендовано в тексте")
            # Пробуем еще раз с другим k
            k = random.randint(2, n - 2)
            P = scalar_mult(k, (Gx, Gy), a, p)
            hP = scalar_mult(h, P, a, p)
            if hP is None:
                print("  ❌ Повторная попытка не удалась → возможно, проблема в параметрах")
                return False
            else:
                print("  ✓ [h]P ≠ O после повторной попытки")
        else:
            print("  ✓ [h]P ≠ O")
    
    print("\n✓ Все проверки порядка успешно пройдены!")
    return True


