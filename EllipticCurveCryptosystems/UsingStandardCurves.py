# ============================================================================
# Арифметика эллиптических кривых над простым полем
# ============================================================================

def modinv(a, p):
    """Обратное число по модулю p (расширенный алгоритм Евклида)"""
    if a == 0:
        return 0
    lm, hm = 1, 0
    low, high = a % p, p
    while low > 1:
        ratio = high // low
        nm = hm - lm * ratio
        new = high - low * ratio
        hm, lm = lm, nm
        high, low = low, new
    return lm % p


def point_add(P, Q, a, p):
    """Сложение двух точек на эллиптической кривой"""
    if P is None:
        return Q
    if Q is None:
        return P
    
    x1, y1 = P
    x2, y2 = Q
    
    if x1 == x2 and y1 != y2:
        return None  # Точка на бесконечности
    
    if x1 == x2 and y1 == y2:
        # Удвоение точки
        m = (3 * x1 * x1 + a) * modinv(2 * y1, p) % p
    else:
        # Сложение разных точек
        m = (y2 - y1) * modinv(x2 - x1, p) % p
    
    x3 = (m * m - x1 - x2) % p
    y3 = (m * (x1 - x3) - y1) % p
    
    return (x3, y3)


def scalar_mult(k, P, a, p):
    """Умножение точки на скаляр (двоичный метод)"""
    result = None
    addend = P
    
    while k:
        if k & 1:
            result = point_add(result, addend, a, p)
        addend = point_add(addend, addend, a, p)
        k >>= 1
    
    return result


