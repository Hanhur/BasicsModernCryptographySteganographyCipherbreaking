    bit_length = n.bit_length()
    print(f"  ✓ n имеет {bit_length} бит (соответствует 256-битной безопасности)")
    
    # Проверка, что n близко к p (для безопасности против аномальных кривых)
    diff = abs(n - p)
    print(f"  ✓ |n - p| = {diff} (очень мало, как и ожидается)")
    
    # Проверка кофактора
    print(f"  ✓ h = {h} (кривая имеет простой порядок, что хорошо)")
    
    # Проверка дискриминанта
    # D = 16*(4*a^3 + 27*b^2) ≠ 0 (для исключения сингулярных кривых)
