# ПРОВЕРКА 2: Проверка принадлежности точки G кривой
# ============================================================================

def check_point_on_curve(Gx, Gy, a, b, p):
    """Проверка, удовлетворяет ли точка G уравнению Y ^ 2 = X ^ 3 + a * X + b (mod p)"""
    print("\n" + "=" * 70)
    print("ПРОВЕРКА 2: Принадлежность точки G кривой")
    print("=" * 70)
    
    # Вычисляем левую часть: Y^2
    left = (Gy * Gy) % p
    
    # Вычисляем правую часть: X^3 + a*X + b
    right = (Gx * Gx * Gx + a * Gx + b) % p
    
    print(f"  Y ^ 2 mod p = {hex(left)}")
    print(f"  X ^ 3 + aX + b mod p = {hex(right)}")
    print(f"  Равенство: {left == right}")
    
    if left == right:
        print("✓ Точка G лежит на кривой")
        return True
    else:
        print("❌ Точка G НЕ лежит на кривой!")
        return False


