"""
Демонстрационная программа: Сравнение стойкости RSA и ECC
На основе текста о криптографии на эллиптических кривых.

Иллюстрирует ключевое утверждение:
"Стойкость RSA с 1024-битным модулем ≈ Стойкости ECC с модулем в 160 бит"
"""

import math
import time

def rsa_key_strength(bits):
    """
    Оценка сложности взлома RSA (субэкспоненциальный рост).
    Используем приближенную формулу для сложности решета числового поля (GNFS):
    Ln[1/3, 1.923] ~ exp(1.923 * (ln(n)) ^ (1 / 3) * (ln(ln(n))) ^ (2 / 3))
    
    Возвращает логарифм числа операций для наглядности.
    """
    n = 2 ** bits
    ln_n = math.log(n)
    # Упрощенная оценка сложности в битах
    complexity = 1.923 * (ln_n ** (1 / 3)) * (math.log(ln_n) ** (2 / 3))
    return complexity

def ecc_key_strength(bits):
    """
    Оценка сложности взлома ECC (экспоненциальный рост).
    Для эллиптических кривых лучший алгоритм - Pollard's Rho.
    Сложность: O(sqrt(pi * n) / 2) ~ примерно 2 ^ (bits / 2)
    """
    # Сложность в битах = bits / 2
    return bits / 2

def find_equivalent_ecc_bits(rsa_bits):
    """
    Находит размер ключа ECC, эквивалентный по стойкости заданному RSA.
    Решает уравнение: rsa_strength(rsa_bits) == ecc_strength(ecc_bits)
    """
    target_strength = rsa_key_strength(rsa_bits)
    # ECC стойкость = ecc_bits / 2, значит ecc_bits = 2 * target_strength
    ecc_bits = int(2 * target_strength)
    return ecc_bits

