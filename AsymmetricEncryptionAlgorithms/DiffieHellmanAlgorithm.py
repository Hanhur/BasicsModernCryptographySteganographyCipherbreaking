def demonstrate_eavesdropper():
    """
    Демонстрация того, что видит злоумышленник (Ева).
    """
    print("=" * 50)
    print("ЧАСТЬ 3: ЧТО ВИДИТ ЗЛОУМЫШЛЕННИК (ЕВА)?")
    print("=" * 50)
    
    p = 23
    g = 5
    
    print(f"Ева перехватывает открытые параметры: p = {p}, g = {g}")
    print()
    
    # Симулируем обмен
    a = 6   # Секрет Алисы
    b = 15  # Секрет Боба
    
    A = pow(g, a, p)  # 5^6 mod 23 = 8
    B = pow(g, b, p)  # 5^15 mod 23 = 19
    
    print(f"Ева видит публичный ключ Алисы: A = {A}")
    print(f"Ева видит публичный ключ Боба:   B = {B}")
    print()
    
    print("ПРОБЛЕМА ЕВЫ:")
    print(f"  Ева знает: p = {p}, g = {g}, A = {A}, B = {B}")
    print(f"  Еве НЕИЗВЕСТНО: a и b (секретные ключи)")
    print()
    print("Чтобы вычислить общий секрет K, Еве нужно решить:")
    print(f"  Найти a из уравнения: 5 ^ a mod 23 = {A}")
    print(f"  ИЛИ найти b из уравнения: 5 ^ b mod 23 = {B}")
    print()
    print("Это задача дискретного логарифмирования!")
    print("Для маленького p = 23 это легко сделать перебором.")
    print("Но когда p имеет 2048 бит (~617 десятичных цифр),")
    print("это становится вычислительно невозможным.")
    print()


