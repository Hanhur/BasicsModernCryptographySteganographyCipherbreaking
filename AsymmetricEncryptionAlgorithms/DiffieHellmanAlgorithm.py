def is_prime(n, k = 5):
    """
    Тест Миллера-Рабина для проверки простоты числа.
    k - количество раундов тестирования (чем больше, тем точнее).
    """
    if n < 2:
        return False
    if n in (2, 3):
        return True
    if n % 2 == 0:
        return False
    
    # Представляем n-1 как d * 2^r
    r, d = 0, n - 1
    while d % 2 == 0:
        r += 1
        d //= 2
    
    # Проверяем k раз
    for _ in range(k):
        a = random.randint(2, n - 2)
        x = pow(a, d, n)
        if x == 1 or x == n - 1:
            continue
        for _ in range(r - 1):
            x = pow(x, 2, n)
            if x == n - 1:
                break
        else:
            return False
    return True


def generate_large_prime(bits = 16):
    """
    Генерация большого простого числа заданной битности.
    Для демонстрации используем 16 бит (числа до 65535).
    В реальности используются 2048-4096 бит.
    """
    while True:
        # Генерируем нечетное число
        candidate = random.getrandbits(bits)
        # Убеждаемся, что число нечетное и достаточно большое
        candidate |= (1 << bits - 1) | 1
        
        if is_prime(candidate):
            return candidate


def find_primitive_root(p):
    """
    Находит первообразный корень по модулю p (примитивный корень).
    Упрощенная версия для демонстрации.
    """
    if p == 2:
        return 1
    
    # Получаем разложение p-1 на простые множители
    factors = []
    phi = p - 1
    n = phi
    
    i = 2
    while i * i <= n:
        if n % i == 0:
            factors.append(i)
            while n % i == 0:
                n //= i
        i += 1
    if n > 1:
        factors.append(n)
    
    # Проверяем кандидатов
    for g in range(2, p):
        is_primitive = True
        for factor in factors:
            if pow(g, phi // factor, p) == 1:
                is_primitive = False
                break
        if is_primitive:
            return g
    
    return None


def real_diffie_hellman():
    """
    Реализация Диффи-Хеллмана с большими простыми числами.
    """
    print("=" * 50)
    print("ЧАСТЬ 2: РЕАЛЬНЫЙ СЦЕНАРИЙ (С ГЕНЕРАЦИЕЙ ПРОСТЫХ ЧИСЕЛ)")
    print("=" * 50)
    
    # Генерируем большое простое число p (16 бит для скорости)
    print("Генерация простого числа p...")
    p = generate_large_prime(bits = 16)
    print(f"  p = {p}")
    
    # Находим первообразный корень g
    print("Поиск первообразного корня g...")
    g = find_primitive_root(p)
    print(f"  g = {g}")
    print()
    
    # Генерируем секретные ключи (большие случайные числа)
    a = random.randint(2, p - 2)
    b = random.randint(2, p - 2)
    
    print(f"Секретный ключ Алисы (a) = {a}")
    print(f"Секретный ключ Боба (b)   = {b}")
    print()
    
    # Вычисляем публичные ключи
    A = pow(g, a, p)
    B = pow(g, b, p)
    
    print(f"Публичный ключ Алисы (A) = {A}")
    print(f"Публичный ключ Боба (B)   = {B}")
    print()
    
    # Вычисляем общий секрет
    K_alice = pow(B, a, p)
    K_bob = pow(A, b, p)
    
    print(f"Общий секрет (Алиса) = {K_alice}")
    print(f"Общий секрет (Боб)   = {K_bob}")
    print()
    
    # Проверка
    if K_alice == K_bob:
        print("✓ СЕКРЕТ УСПЕШНО СОГЛАСОВАН!")
        print(f"  Общий ключ: {K_alice}")
        
        # Демонстрация: получаем ключ шифрования из общего секрета
        key_bytes = str(K_alice).encode()
        hash_key = hashlib.sha256(key_bytes).hexdigest()[:16]
        print(f"  Ключ для шифрования (SHA-256): {hash_key}")
    else:
        print("✗ ОШИБКА: Ключи не совпадают!")
    
    print()
    return K_alice


