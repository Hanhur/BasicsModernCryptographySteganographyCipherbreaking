def simulate_diffie_hellman():
    """
    Симуляция алгоритма Диффи-Хеллмана с маленькими числами.
    Это точное математическое отображение мысленного эксперимента с ящиком.
    """
    print("=" * 50)
    print("ЧАСТЬ 1: СИМУЛЯЦИЯ С МАЛЕНЬКИМИ ЧИСЛАМИ")
    print("=" * 50)
    
    # Открытые параметры (известны всем, включая Еву)
    p = 23      # Простое число (модуль) - это "пустой ящик"
    g = 5       # Первообразный корень - это "форма ящика"
    
    print(f"Открытые параметры (известны всем):")
    print(f"  p (простое число) = {p}")
    print(f"  g (основание)     = {g}")
    print()
    
    # Секретные ключи (личные замки)
    a = random.randint(2, p-2)  # Секретный ключ Алисы
    b = random.randint(2, p-2)  # Секретный ключ Боба
    
    print(f"Секретные ключи (никто не знает):")
    print(f"  a (секрет Алисы) = {a}")
    print(f"  b (секрет Боба)   = {b}")
    print()
    
    # Шаг 1: Алиса вычисляет публичный ключ и отправляет Бобу
    A = pow(g, a, p)  # g^a mod p
    print(f"ШАГ 1: Алиса отправляет Бобу A = g ^ a mod p = {A}")
    
    # Шаг 2: Боб вычисляет публичный ключ и отправляет Алисе
    B = pow(g, b, p)  # g^b mod p
    print(f"ШАГ 2: Боб отправляет Алисе B = g ^ b mod p = {B}")
    print()
    
    # Шаг 3: Алиса вычисляет общий секрет (снимает свой замок)
    K_alice = pow(B, a, p)  # (g^b)^a mod p = g^(ab) mod p
    print(f"ШАГ 3: Алиса вычисляет K = B ^ a mod p = {K_alice}")
    
    # Шаг 4: Боб вычисляет общий секрет (снимает свой замок)
    K_bob = pow(A, b, p)    # (g^a)^b mod p = g^(ab) mod p
    print(f"ШАГ 4: Боб вычисляет K = A ^ b mod p = {K_bob}")
    print()
    
    # Проверка: совпадают ли ключи?
    print(f"РЕЗУЛЬТАТ: Общий секретный ключ = {K_alice}")
    print(f"Ключи совпадают? {'ДА ✓' if K_alice == K_bob else 'НЕТ ✗'}")
    print()
    
    return K_alice


