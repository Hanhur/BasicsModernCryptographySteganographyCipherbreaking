def encrypt_message(message):
    """Шифрует русский текст в иероглифы (для игры)"""
    # Простая замена: берем первые буквы слов и превращаем в иероглифы
    hieroglyph_chars = list(HIEROGLYPH_DICT.keys())
    result = []
    for char in message.upper():
        if char.isalpha():
            # Берем случайный иероглиф, соответствующий букве (для простоты)
            # В реальности мы бы брали точное соответствие, но это игра
            if char in "ABCDEFGHIJKLMNOPQRSTUVWXYZ":
                # Ищем любой иероглиф с этим звуком
                for h, val in HIEROGLYPH_DICT.items():
                    if val[0] == char:
                        result.append(h)
                        break
                else:
                    result.append("𓂝")  # fallback
        else:
            result.append(" ")
    return "".join(result)

def decode_hieroglyphs(text):
    """Декодирует иероглифы в звуки (главная функция дешифровки)"""
    decoded = []
    i = 0
    while i < len(text):
        # Проверяем двухсимвольные иероглифы (KH, SH)
        if i + 1 < len(text) and text[i:i + 2] in ["𓐍", "𓈙"]:
            # Это особые случаи
            if text[i:i + 2] == "𓐍":
                decoded.append("KH")
            elif text[i:i + 2] == "𓈙":
                decoded.append("SH")
            i += 2
        else:
            # Одиночный иероглиф
            glyph = text[i]
            if glyph in HIEROGLYPH_DICT:
                decoded.append(HIEROGLYPH_DICT[glyph])
            else:
                decoded.append("?")
            i += 1
    return "".join(decoded)

def translate_phrase(hieroglyphs):
    """Пытается перевести целую фразу"""
    if hieroglyphs in KNOWN_PHRASES:
        return KNOWN_PHRASES[hieroglyphs]
    else:
        # Пытаемся декодировать по буквам
        decoded = decode_hieroglyphs(hieroglyphs)
        return f"Расшифровано: {decoded} (возможно, имя или слово)"

