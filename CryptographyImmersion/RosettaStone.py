def clear_screen():
    """Очистка экрана (для красоты)"""
    os.system('cls' if os.name == 'nt' else 'clear')

def typewriter_effect(text, delay = 0.03):
    """Эффект печатающей машинки (как в старых квестах)"""
    for char in text:
        print(char, end = '', flush = True)
        time.sleep(delay)
    print()

def slow_print(text, delay = 0.05):
    """Медленная печать с эффектом"""
    for char in text:
        print(char, end = '', flush = True)
        time.sleep(delay)
    print()

