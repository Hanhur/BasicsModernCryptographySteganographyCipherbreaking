# ==================== ВСПОМОГАТЕЛЬНЫЕ ФУНКЦИИ ====================

def permute(block, table):
    """Перестановка битов согласно таблице"""
    return [block[i - 1] for i in table]

def xor_bits(a, b):
    """Побитовое XOR для двух списков битов"""
    return [x ^ y for x, y in zip(a, b)]

def split_half(block):
    """Разделяет 64-битный блок на две половины по 32 бита"""
    mid = len(block) // 2
    return block[:mid], block[mid:]

def combine_half(left, right):
    """Объединяет две половины в один блок"""
    return left + right

def bits_to_int(bits):
    """Преобразует список битов в целое число"""
    return int(''.join(str(b) for b in bits), 2)

def int_to_bits(value, length):
    """Преобразует целое число в список битов фиксированной длины"""
    return [int(b) for b in format(value, f'0{length}b')]


# ==================== ФУНКЦИЯ ФЕЙСТЕЛЯ f(R, K) ====================

def feistel_function(right, key):
    """
    Функция f(R, K) = P(S(E(R) XOR K))
    right: 32 бита
    key: 48 бит
    Возвращает: 32 бита
    """
    # 1. Расширение E: 32 -> 48 бит
    expanded = permute(right, E)
    
    # 2. XOR с ключом
    xored = xor_bits(expanded, key)
    
    # 3. S-блоки: 48 -> 32 бита
    s_output = []
    for i in range(8):
        # Берем 6 бит для i-го S-блока
        chunk = xored[i * 6:(i + 1) * 6]
        row = (chunk[0] << 1) | chunk[5]   # 1-й и 6-й биты -> строка (0-3)
        col = (chunk[1] << 3) | (chunk[2] << 2) | (chunk[3] << 1) | chunk[4]  # 4 бита -> столбец (0-15)
        value = S_BOX[i][row][col]
        s_output.extend(int_to_bits(value, 4))
    
    # 4. Перестановка P: 32 -> 32 бита
    return permute(s_output, P)


# ==================== ОДИН РАУНД ШИФРА ====================

def des_round(left, right, key):
    """
    Один раунд сети Фейстеля:
    L_new = R
    R_new = L XOR f(R, K)
    """
    new_left = right
    f_result = feistel_function(right, key)
    new_right = xor_bits(left, f_result)
    return new_left, new_right


# ==================== ОСНОВНАЯ ФУНКЦИЯ DES ====================

def des_crypt(block_64, round_keys, decrypt=False):
    """
    Универсальная функция шифрования/расшифрования DES
    
    block_64: список из 64 бит
    round_keys: список из 16 ключей по 48 бит
    decrypt: если True, ключи подаются в обратном порядке
    """
    if decrypt:
        round_keys = round_keys[::-1]  # Ключи в обратном порядке
    
    # 1. Начальная перестановка IP
    block = permute(block_64, IP)
    
    # 2. Разделение на L0 и R0
    left, right = split_half(block)
    
    # 3. 16 раундов
    for i in range(16):
        left, right = des_round(left, right, round_keys[i])
    
    # 4. После 16 раундов меняем местами (L16, R16) -> (R16, L16)
    #    Это эквивалентно тому, что в конце шифрования нет перестановки,
    #    а в расшифровании она учтена
    block = combine_half(right, left)
    
    # 5. Финальная перестановка IP^-1
    result = permute(block, IP_INV)
    
    return result


