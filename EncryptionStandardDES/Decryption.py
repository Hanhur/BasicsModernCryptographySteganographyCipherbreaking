# Для демонстрации используем фиксированный набор ключей (в реальном DES
# они генерируются из 56-битного ключа через PC-1, сдвиги и PC-2).
# Здесь создаем 16 псевдослучайных 48-битных ключей для теста.

def generate_test_keys():
    """Генерирует 16 тестовых ключей по 48 бит (для демонстрации)"""
    import random
    random.seed(42)  # Фиксируем seed для воспроизводимости
    keys = []
    for _ in range(16):
        # Генерируем 48 случайных бит
        key = [random.randint(0, 1) for _ in range(48)]
        keys.append(key)
    return keys


# ==================== ПРИМЕР ИСПОЛЬЗОВАНИЯ ====================

def text_to_bits(text):
    """Преобразует строку в 64-битный блок"""
    # Берем первые 8 символов, дополняем пробелами до 8
    text = text[:8].ljust(8)
    bits = []
    for char in text:
        bits.extend(int_to_bits(ord(char), 8))
    return bits

def bits_to_text(bits):
    """Преобразует 64-битный блок в строку (8 символов)"""
    chars = []
    for i in range(8):
        byte = bits[i * 8:(i + 1) * 8]
        chars.append(chr(bits_to_int(byte)))
    return ''.join(chars)


