# ============ ВСПОМОГАТЕЛЬНЫЕ ФУНКЦИИ ============

def string_to_bits(text: str) -> str:
    """Преобразует строку в битовую строку (8 бит на символ)"""
    bits = ''
    for char in text:
        bits += format(ord(char), '08b')
    return bits

def bits_to_string(bits: str) -> str:
    """Преобразует битовую строку обратно в строку"""
    text = ''
    for i in range(0, len(bits), 8):
        byte = bits[i:i + 8]
        text += chr(int(byte, 2))
    return text

def hex_to_bits(hex_str: str) -> str:
    """Преобразует шестнадцатеричную строку в битовую"""
    bits = ''
    for h in hex_str:
        bits += format(int(h, 16), '04b')
    return bits

def bits_to_hex(bits: str) -> str:
    """Преобразует битовую строку в шестнадцатеричную"""
    hex_str = ''
    for i in range(0, len(bits), 4):
        hex_str += format(int(bits[i:i + 4], 2), 'X')
    return hex_str

def permute(bits: str, table: list) -> str:
    """Выполняет перестановку битов по таблице"""
    return ''.join(bits[i - 1] for i in table)

def xor(a: str, b: str) -> str:
    """Побитовое XOR двух битовых строк"""
    return ''.join('1' if x != y else '0' for x, y in zip(a, b))

def left_shift(bits: str, n: int) -> str:
    """Циклический сдвиг влево на n позиций"""
    return bits[n:] + bits[:n]

def split_half(bits: str) -> tuple:
    """Разделяет битовую строку на две половины"""
    mid = len(bits) // 2
    return bits[:mid], bits[mid:]

# ============ ОСНОВНЫЕ ФУНКЦИИ DES ============

def generate_round_keys(key_64: str) -> list:
    """
    Генерирует 16 раундовых ключей по 48 бит из 64-битного ключа
    """
    # Проверка длины ключа
    if len(key_64) != 64:
        raise ValueError("Ключ должен быть 64 бита")
    
    # Шаг 1: Сжатие ключа до 56 бит (PC-1) - удаление битов четности
    key_56 = permute(key_64, PC1)
    
    # Шаг 2: Разделение на две половины по 28 бит
    c, d = split_half(key_56)
    
    round_keys = []
    for shift in SHIFTS:
        # Циклический сдвиг обеих половин
        c = left_shift(c, shift)
        d = left_shift(d, shift)
        
        # Объединение и сжатие до 48 бит (PC-2)
        combined = c + d
        round_key = permute(combined, PC2)
        round_keys.append(round_key)
    
    return round_keys

def s_box_substitution(bits_48: str) -> str:
    """
    Преобразует 48 бит в 32 бита через S-блоки
    """
    result = ''
    
    for i in range(8):
        # Берем 6 бит для текущего S-блока
        chunk = bits_48[i * 6:(i + 1) * 6]
        
        # Определяем строку (биты 0 и 5) и столбец (биты 1-4)
        row = int(chunk[0] + chunk[5], 2)
        col = int(chunk[1:5], 2)
        
        # Получаем значение из S-блока (4 бита)
        val = S_BOXES[i][row][col]
        result += format(val, '04b')
    
    return result

def des_round(left: str, right: str, round_key: str) -> tuple:
    """
    Один раунд шифрования DES
    """
    # 1. Расширение правой части с 32 до 48 бит
    expanded = permute(right, E)
    
    # 2. XOR с раундовым ключом
    xored = xor(expanded, round_key)
    
    # 3. Подстановка через S-блоки (48 -> 32 бит)
    substituted = s_box_substitution(xored)
    
    # 4. Перестановка P (32 -> 32 бит)
    permuted = permute(substituted, P)
    
    # 5. Новая правая часть = левая XOR результат
    new_right = xor(left, permuted)
    new_left = right
    
    return new_left, new_right

def des_encrypt_block(block_64: str, key_64: str) -> str:
    """
    Шифрует 64-битный блок с использованием 64-битного ключа
    """
    if len(block_64) != 64:
        raise ValueError("Блок должен быть 64 бита")
    if len(key_64) != 64:
        raise ValueError("Ключ должен быть 64 бита")
    
    # 1. Начальная перестановка IP
    block = permute(block_64, IP)
    
    # 2. Разделение на левую и правую части
    left, right = split_half(block)
    
    # 3. Генерация раундовых ключей
    round_keys = generate_round_keys(key_64)
    
    # 4. 16 раундов шифрования
    for i in range(16):
        left, right = des_round(left, right, round_keys[i])
    
    # 5. Объединение (обратный порядок перед финальной перестановкой)
    combined = right + left
    
    # 6. Финальная перестановка IP^-1
    ciphertext = permute(combined, IP_INV)
    
    return ciphertext

def des_encrypt_text(plaintext: str, key: str) -> str:
    """
    Шифрует текстовую строку (дополняет до кратности 8 символов)
    """
    # Преобразуем ключ в биты (должен быть ровно 8 символов)
    key_bits = string_to_bits(key)
    if len(key_bits) != 64:
        # Дополняем или обрезаем ключ до 64 бит
        key_bits = key_bits.ljust(64, '0')[:64]
    
    # Преобразуем текст в биты
    text_bits = string_to_bits(plaintext)
    
    # Дополняем текст до кратности 64 бит (8 байт)
    padding_len = (64 - len(text_bits) % 64) % 64
    text_bits += '0' * padding_len
    
    # Шифруем по блокам
    cipher_bits = ''
    for i in range(0, len(text_bits), 64):
        block = text_bits[i:i+64]
        encrypted_block = des_encrypt_block(block, key_bits)
        cipher_bits += encrypted_block
    
    return bits_to_hex(cipher_bits)

