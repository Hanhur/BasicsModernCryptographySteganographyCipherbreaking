# 8. Анализ влияния размера ключа на семантическую секретность
# ------------------------------------------------------------
def demonstrate_weak_vs_strong_security():
    """
    Показывает, почему малые k не обеспечивают семантическую секретность,
    а большие могут (при правильной схеме).
    """
    print("\n" + "=" * 60)
    print("Анализ: почему реальная семантическая секретность требует")
    print("вероятностного шифрования и больших параметров")
    print("=" * 60)
    
    # Сценарий 1: Маленькое k (плохо)
    k_small = 5
    print(f"\n1. Маленький параметр безопасности k = {k_small}:")
    print(f"   - Пространство сообщений: 2 ^ {k_small} = {2 ** k_small} вариантов")
    print(f"   - B может перебрать почти все сообщения за разумное время")
    print("   → Разность вероятностей ВЕЛИКА → НЕ семантически секретно")
    
    # Сценарий 2: Большое k + детерминированное шифрование (все равно не семантически секретно)
    k_large = 20
    print(f"\n2. Большой k = {k_large}, но шифрование детерминированное (RSA без padding):")
    print(f"   - B может проверить конкретную гипотезу m' (например, 'да' или 'нет')")
    print(f"   - Поскольку E(m') однозначно соответствует c, B угадывает с вероятностью 1")
    print(f"   - C угадывает с вероятностью ~0.5 (если f(m) равновероятно)")
    print("   → Разность ≈ 0.5 → НЕ семантически секретно")
    
    # Сценарий 3: Правильная семантическая секретность (вероятностное шифрование + большой k)
    print(f"\n3. Правильный подход (IND-CPA безопасная схема, например, ElGamal):")
    print(f"   - Шифрование вероятностное: одному m → много разных c")
    print(f"   - B не может просто проверить m' → приходится угадывать")
    print(f"   - При k → ∞ разность |p(B)-p(C)| → 0 (пренебрежимо)")
    print("   → Семантически секретно!")

