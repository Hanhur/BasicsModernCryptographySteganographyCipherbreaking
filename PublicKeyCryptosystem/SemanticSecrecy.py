# 6. Семантическая секретность
import random
import math

# ------------------------------------------------------------
# 1. Генератор сообщений A(k)
# ------------------------------------------------------------
def A(k):
    """
    Генерирует сообщение m по параметру безопасности k.
    В данном примере: m - целое число от 0 до 2^k - 1.
    """
    # Чем больше k, тем больше пространство сообщений
    max_value = 2 ** k
    return random.randint(0, max_value - 1)

# ------------------------------------------------------------
# 2. Функция f(m) (то, что хочет узнать атакующий)
# ------------------------------------------------------------
def f(m):
    """
    Пример: функция - четность количества единичных битов.
    Возвращает 1, если число единиц в двоичной записи нечетно, иначе 0.
    """
    return bin(m).count('1') % 2

# ------------------------------------------------------------
# 3. Система шифрования E(m, key)
# ------------------------------------------------------------
def E(m, key):
    """
    Упрощенная модель шифрования.
    Реальное шифрование было бы сложнее (RSA, Эль-Гамаль и т.д.)
    Здесь: c = (m * key) mod (big_prime)
    """
    # Простое число для модуля (упрощение)
    modulus = 10 ** 9 + 7
    return (m * key) % modulus

# ------------------------------------------------------------
# 4. Алгоритм B (атакующий, знает шифровку)
# ------------------------------------------------------------
def B(c, e, k):
    """
    B пытается угадать f(m), зная шифровку c и открытый ключ e.
    
    В реальной семантической секретности B может быть любым
    полиномиальным алгоритмом. Здесь упрощенно:
    - B перебирает небольшое количество кандидатов m'
    - Проверяет, совпадает ли E(m', e) с c
    - Если находит, возвращает f(m')
    - Иначе угадывает случайно (1/2)
    """
    modulus = 10 ** 9 + 7
    candidates_tested = 0
    max_candidates = min(2 ** k, 100)  # Ограничиваем перебор для демонстрации
    
    # Проверяем несколько возможных сообщений
    # В реальной системе у B нет времени перебирать все 2^k вариантов,
    # но здесь мы делаем упрощение для демонстрации идеи.
    for candidate in range(max_candidates):
        if E(candidate, e) == c:
            # Нашли m, совпадающее с шифровкой
            return f(candidate)
        candidates_tested += 1
    
    # Если не нашли точное совпадение (в вероятностном шифровании 
    # одного m -> много c, но здесь детерминированное шифрование,
    # поэтому мы находим всегда. В реальности B может использовать
    # статистические методы.)
    # Для демонстрации: угадываем случайно
    return random.randint(0, 1)

# ------------------------------------------------------------
# 5. Алгоритм C (имитатор, не знает шифровку)
# ------------------------------------------------------------
def C(k):
    """
    C пытается угадать f(m), НЕ ЗНАЯ шифровки c,
    только зная параметр k и распределение A(k).
    
    Он может использовать априорное распределение.
    Здесь: оценивает вероятность f(m) = 1, основываясь на частоте
    появления четности в сообщениях.
    """
    # Оцениваем априорное распределение, сгенерировав много сообщений
    sample_size = 1000
    count_ones = 0
    
    for _ in range(sample_size):
        test_m = A(k)
        if f(test_m) == 1:
            count_ones += 1
    
    prob_f_is_one = count_ones / sample_size
    
    # Оптимальная стратегия: угадывать то значение f(m),
    # которое более вероятно априори
    if prob_f_is_one > 0.5:
        return 1
    else:
        return 0

# ------------------------------------------------------------
# 6. Оценка вероятностей p(B,f) и p(C,f)
# ------------------------------------------------------------
def estimate_probabilities(num_trials, k, e):
    """
    Оценивает вероятности успеха B и C.
    """
    success_B = 0
    success_C = 0
    
    for _ in range(num_trials):
        # Генерируем сообщение
        m = A(k)
        correct_f = f(m)
        
        # Шифруем
        c = E(m, e)
        
        # B угадывает с шифровкой
        guess_B = B(c, e, k)
        if guess_B == correct_f:
            success_B += 1
        
        # C угадывает без шифровки
        guess_C = C(k)
        if guess_C == correct_f:
            success_C += 1
    
    pB = success_B / num_trials
    pC = success_C / num_trials
    
    return pB, pC

# ------------------------------------------------------------
# 7. Главная функция: проверка семантической секретности
# ------------------------------------------------------------
def check_semantic_security(k_values, num_trials_per_k = 1000):
    """
    Проверяет условие семантической секретности для разных k.
    """
    print("Проверка семантической секретности")
    print("=" * 60)
    print(f"{'k':<6} {'p(B,f)':<10} {'p(C,f)':<10} {'|p(B)-p(C)|':<15} {'Семантически секретно?'}")
    print("-" * 60)
    
    for k in k_values:
        # Генерируем случайный открытый ключ e
        e = random.randint(2, 1000)
        
        pB, pC = estimate_probabilities(num_trials_per_k, k, e)
        diff = abs(pB - pC)
        
        # Порог пренебрежимости: 1 / k^2 (пример)
        # В реальности пренебрежимая функция убывает быстрее любого полинома
        negligible_threshold = 1.0 / (k ** 2)
        
        is_secure = diff < negligible_threshold
        
        print(f"{k:<6} {pB:<10.4f} {pC:<10.4f} {diff:<15.4f} {is_secure}")
        
        # Дополнительное пояснение для k больших
        if k >= 10:
            print(f"  → При k = {k} разность {diff:.4f} {'меньше' if is_secure else 'больше'} порога {negligible_threshold:.6f}")

