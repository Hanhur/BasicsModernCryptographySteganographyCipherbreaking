# 5. ОСНОВНЫЕ ФУНКЦИИ ШИФРОВАНИЯ И ДЕШИФРОВАНИЯ
# ============================================================================

def des_encrypt_block(block_bits, round_keys):
    """
    Шифрует один 64-битный блок с помощью DES
    """
    # Убеждаемся, что блок 64 бита
    while len(block_bits) < 64:
        block_bits.append(0)
    block_bits = block_bits[:64]
    
    # Начальная перестановка (IP)
    block = permute(block_bits, IP)
    
    # Разбиваем на левую и правую половины
    left, right = split_bits(block, 32)
    
    # 16 раундов сети Файстеля
    for i in range(16):
        # Сохраняем левую для следующего раунда
        new_left = right
        # Применяем раундовую функцию
        f_result = feistel_round(right, round_keys[i])
        # XOR с левой половиной (основная операция)
        new_right = xor_bits(left, f_result)
        # Переход к следующему раунду
        left, right = new_left, new_right
    
    # Финальная перестановка (обратная IP)
    # После 16 раундов меняем местами L16 и R16
    combined = right + left  # Обратите внимание: right + left, а не left + right
    return permute(combined, FP)

def des_decrypt_block(block_bits, round_keys):
    """
    Дешифрует один 64-битный блок
    Использует те же ключи, но в обратном порядке (свойство сети Файстеля)
    """
    # Убеждаемся, что блок 64 бита
    while len(block_bits) < 64:
        block_bits.append(0)
    block_bits = block_bits[:64]
    
    # Начальная перестановка (IP)
    block = permute(block_bits, IP)
    
    # Разбиваем на левую и правую половины
    left, right = split_bits(block, 32)
    
    # 16 раундов сети Файстеля (ключи в обратном порядке)
    for i in range(15, -1, -1):
        new_left = right
        f_result = feistel_round(right, round_keys[i])
        new_right = xor_bits(left, f_result)
        left, right = new_left, new_right
    
    # Финальная перестановка
    combined = right + left
    return permute(combined, FP)

