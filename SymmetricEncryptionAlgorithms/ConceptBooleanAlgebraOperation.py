def feistel_round(right_bits, round_key):
    """
    Раундовая функция F (сеть Файстеля):
    1. Расширение (E) - 32 бита -> 48 бит
    2. XOR с раундовым ключом
    3. S-блоки (нелинейное замешивание) - 48 бит -> 32 бита
    4. Перестановка P (рассеивание)
    
    Это сердце алгоритма, где применяются все операции булевой алгебры
    """
    # Убеждаемся, что right_bits 32 бита
    while len(right_bits) < 32:
        right_bits.append(0)
    right_bits = right_bits[:32]
    
    # 1. Расширение: 32 -> 48 бит (рассеивание)
    expanded = permute(right_bits, E)
    
    # 2. XOR с ключом: сложение по модулю 2 (ОСНОВНАЯ ОПЕРАЦИЯ)
    xored = xor_bits(expanded, round_key)
    
    # 3. S-блоки: 48 бит -> 32 бита (НЕЛИНЕЙНОЕ ЗАМЕШИВАНИЕ)
    s_output = []
    for i in range(8):
        # Берем 6 бит для текущего S-блока
        six_bits = xored[i * 6 : (i + 1) * 6]
        # Применяем S-блок (6 -> 4 бита)
        four_bits = s_box_lookup(i, six_bits)
        s_output.extend(four_bits)
    
    # 4. Перестановка P (рассеивание)
    return permute(s_output, P)

