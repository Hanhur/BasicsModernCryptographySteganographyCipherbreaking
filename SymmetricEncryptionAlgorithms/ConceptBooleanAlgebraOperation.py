def string_to_bits(text):
    """Преобразует строку в битовый список (8 бит на символ)"""
    bits = []
    for char in text:
        # Получаем ASCII код и переводим в 8-битное двоичное представление
        for i in range(7, -1, -1):
            bits.append((ord(char) >> i) & 1)
    return bits

def bits_to_string(bits):
    """Преобразует битовый список обратно в строку"""
    # Дополняем нулями до кратного 8
    while len(bits) % 8 != 0:
        bits.append(0)
    
    chars = []
    for i in range(0, len(bits), 8):
        byte = 0
        for j in range(8):
            byte = (byte << 1) | bits[i + j]
        chars.append(chr(byte))
    return ''.join(chars)

def hex_to_bits(hex_str):
    """Преобразует шестнадцатеричную строку в битовый список"""
    bits = []
    for h in hex_str:
        val = int(h, 16)
        for i in range(3, -1, -1):
            bits.append((val >> i) & 1)
    return bits

def bits_to_hex(bits):
    """Преобразует битовый список в шестнадцатеричную строку"""
    # Дополняем нулями до кратного 4
    while len(bits) % 4 != 0:
        bits.append(0)
    
    hex_str = ''
    for i in range(0, len(bits), 4):
        val = 0
        for j in range(4):
            val = (val << 1) | bits[i + j]
        hex_str += hex(val)[2:].upper()
    return hex_str

def permute(bits, table):
    """
    Функция перестановки (рассеивание)
    Применяет таблицу перестановки к битовому списку
    """
    # Убеждаемся, что bits достаточно длинный
    max_idx = max(table)
    if len(bits) < max_idx:
        # Если битов недостаточно, дополняем нулями
        bits = bits + [0] * (max_idx - len(bits))
    
    return [bits[i - 1] for i in table]

def xor_bits(bits1, bits2):
    """
    Логическая операция XOR (исключающее ИЛИ) - сложение по модулю 2
    Это ОСНОВНАЯ операция в DES, согласно вашему тексту
    """
    # Выравниваем длины
    max_len = max(len(bits1), len(bits2))
    bits1 = bits1 + [0] * (max_len - len(bits1))
    bits2 = bits2 + [0] * (max_len - len(bits2))
    
    return [b1 ^ b2 for b1, b2 in zip(bits1, bits2)]

def split_bits(bits, n):
    """Разбивает список битов на две части по n бит"""
    return bits[:n], bits[n:]

def left_shift(bits, n):
    """Циклический сдвиг битового списка влево на n позиций"""
    if len(bits) == 0:
        return bits
    n = n % len(bits)
    return bits[n:] + bits[:n]

def s_box_lookup(s_box_num, six_bits):
    """
    S-блок: НЕЛИНЕЙНОЕ ПРЕОБРАЗОВАНИЕ 6 бит -> 4 бита
    Это ключевая операция "замешивания" (confusion)
    
    Как это работает согласно вашему тексту:
    - 1-й и 6-й биты образуют номер строки (row)
    - 4 средних бита образуют номер столбца (col)
    - Значение из таблицы S-блока дает 4-битный выход
    """
    # Убеждаемся, что у нас 6 бит
    while len(six_bits) < 6:
        six_bits.append(0)
    six_bits = six_bits[:6]
    
    # Строка: биты 0 и 5 (в 6-битной группе)
    row = (six_bits[0] << 1) | six_bits[5]
    # Столбец: биты 1, 2, 3, 4
    col = (six_bits[1] << 3) | (six_bits[2] << 2) | (six_bits[3] << 1) | six_bits[4]
    
    # Получаем значение из S-блока (0-15)
    value = S_BOXES[s_box_num][row][col]
    
    # Превращаем в 4 бита
    return [(value >> 3) & 1, (value >> 2) & 1, (value >> 1) & 1, value & 1]

