def generate_round_keys(key_bits):
    """
    Генерирует 16 раундовых ключей по 48 бит из 64-битного ключа
    (реальные 56 бит + 8 бит четности)
    """
    # Убеждаемся, что ключ 64 бита
    while len(key_bits) < 64:
        key_bits.append(0)
    key_bits = key_bits[:64]
    
    # Начальная перестановка ключа - берем первые 56 бит (упрощенно)
    key_56 = key_bits[:56]
    
    # Разбиваем на две половины по 28 бит (C0 и D0)
    c, d = split_bits(key_56, 28)
    
    round_keys = []
    
    for round_num in range(16):
        # Циклический сдвиг влево на 1 или 2 позиции
        shift = SHIFT_SCHEDULE[round_num]
        c = left_shift(c, shift)
        d = left_shift(d, shift)
        
        # Объединяем и применяем сжатие (PC-2) -> 48 бит
        combined = c + d
        round_key = permute(combined, PC2)
        round_keys.append(round_key)
    
    return round_keys

