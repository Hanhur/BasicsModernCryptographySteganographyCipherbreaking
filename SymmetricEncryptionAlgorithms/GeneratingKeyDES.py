# ============================================================================
# ВСПОМОГАТЕЛЬНЫЕ ФУНКЦИИ ДЛЯ РАБОТЫ С БИТАМИ
# ============================================================================

def string_to_bits(text):
    """
    Преобразует строку в битовую строку (8 бит на символ).
    """
    bits = ''
    for char in text:
        # Получаем ASCII код символа и переводим в 8-битный двоичный вид
        bits += format(ord(char), '08b')
    return bits

def bits_to_string(bits):
    """
    Преобразует битовую строку обратно в строку.
    """
    # Дополняем нулями до кратности 8
    if len(bits) % 8 != 0:
        bits += '0' * (8 - len(bits) % 8)
    
    text = ''
    for i in range(0, len(bits), 8):
        byte = bits[i:i + 8]
        text += chr(int(byte, 2))
    return text

def hex_to_bits(hex_str):
    """
    Преобразует шестнадцатеричную строку в битовую строку.
    """
    bits = ''
    for h in hex_str:
        bits += format(int(h, 16), '04b')
    return bits

def bits_to_hex(bits):
    """
    Преобразует битовую строку в шестнадцатеричную строку.
    """
    # Дополняем нулями до кратности 4
    if len(bits) % 4 != 0:
        bits += '0' * (4 - len(bits) % 4)
    
    hex_str = ''
    for i in range(0, len(bits), 4):
        hex_str += format(int(bits[i:i + 4], 2), 'x')
    return hex_str

def xor_bits(a, b):
    """
    Побитовое XOR двух битовых строк одинаковой длины.
    """
    result = ''
    for i in range(len(a)):
        result += '1' if a[i] != b[i] else '0'
    return result

def permute(bits, table):
    """
    Перестановка битов согласно таблице.
    bits - битовая строка
    table - список позиций (индексы с 1)
    """
    result = ''
    for pos in table:
        result += bits[pos - 1]
    return result

def shift_left(bits, n):
    """
    Циклический сдвиг влево на n позиций.
    """
    return bits[n:] + bits[:n]

def split_half(bits):
    """
    Разделяет битовую строку на две равные половины.
    """
    mid = len(bits) // 2
    return bits[:mid], bits[mid:]

def combine_half(left, right):
    """
    Объединяет две половины в одну строку.
    """
    return left + right


# ============================================================================
# ФУНКЦИЯ f(R, K) - ОСНОВНАЯ ФУНКЦИЯ ШИФРОВАНИЯ
# ============================================================================

def feistel_function(R, K):
    """
    Функция f(R, K) для DES.
    R - 32 бита (правая половина)
    K - 48 бит (раундовый ключ)
    Возвращает 32 бита.
    """
    # 1. Расширение E: 32 -> 48 бит
    expanded_R = permute(R, E)
    
    # 2. XOR с раундовым ключом
    xor_result = xor_bits(expanded_R, K)
    
    # 3. S-блоки: 48 -> 32 бита (каждый S-блок преобразует 6 бит в 4)
    sbox_output = ''
    for i in range(8):
        # Берем 6 бит для текущего S-блока
        chunk = xor_result[i * 6:(i + 1) * 6]
        
        # Определяем строку (первые и последние биты) и столбец (средние 4 бита)
        row = int(chunk[0] + chunk[5], 2)
        col = int(chunk[1:5], 2)
        
        # Получаем значение из S-блока (4 бита)
        value = S_BOXES[i][row][col]
        sbox_output += format(value, '04b')
    
    # 4. P-перестановка: 32 -> 32 бита
    result = permute(sbox_output, P)
    
    return result


# ============================================================================
# ГЕНЕРАЦИЯ РАУНДОВЫХ КЛЮЧЕЙ
# ============================================================================

def generate_round_keys(key_64):
    """
    Генерирует 16 раундовых ключей по 48 бит из 64-битного ключа.
    key_64 - битовая строка длиной 64.
    Возвращает список из 16 ключей по 48 бит.
    """
    # 1. Сжатие PC-1: 64 -> 56 бит (удаляем каждый 8-й бит)
    key_56 = permute(key_64, PC1)
    
    # 2. Разделяем на C0 и D0 (по 28 бит)
    C, D = split_half(key_56)
    
    round_keys = []
    
    for i in range(16):
        # 3. Циклический сдвиг влево согласно таблице
        shift = SHIFTS[i]
        C = shift_left(C, shift)
        D = shift_left(D, shift)
        
        # 4. Объединяем и сжимаем PC-2: 56 -> 48 бит
        combined = combine_half(C, D)
        round_key = permute(combined, PC2)
        round_keys.append(round_key)
    
    return round_keys


# ============================================================================
# ШИФРОВАНИЕ DES
# ============================================================================

def des_encrypt_block(plaintext_64, key_64):
    """
    Шифрует 64-битный блок с помощью 64-битного ключа.
    Возвращает 64-битный шифротекст.
    """
    # 1. Начальная перестановка IP
    permuted_block = permute(plaintext_64, IP)
    
    # 2. Разделяем на L0 и R0 (по 32 бита)
    L, R = split_half(permuted_block)
    
    # 3. Генерируем раундовые ключи
    round_keys = generate_round_keys(key_64)
    
    # 4. 16 раундов шифрования
    for i in range(16):
        # L_i = R_{i-1}
        new_L = R
        
        # R_i = L_{i-1} XOR f(R_{i-1}, K_i)
        f_result = feistel_function(R, round_keys[i])
        new_R = xor_bits(L, f_result)
        
        L, R = new_L, new_R
    
    # 5. Перестановка перед финальной перестановкой (меняем местами L16 и R16)
    combined = combine_half(R, L)
    
    # 6. Финальная перестановка IP_INV
    ciphertext = permute(combined, IP_INV)
    
    return ciphertext


# ============================================================================
# РАСШИФРОВАНИЕ DES
# ============================================================================

def des_decrypt_block(ciphertext_64, key_64):
    """
    Расшифровывает 64-битный блок с помощью 64-битного ключа.
    Возвращает 64-битный открытый текст.
    """
    # 1. Начальная перестановка IP
    permuted_block = permute(ciphertext_64, IP)
    
    # 2. Разделяем на L0 и R0
    L, R = split_half(permuted_block)
    
    # 3. Генерируем раундовые ключи в обратном порядке (K16 ... K1)
    round_keys = generate_round_keys(key_64)
    round_keys_reversed = round_keys[::-1]  # Переворачиваем список
    
    # 4. 16 раундов расшифрования
    for i in range(16):
        new_L = R
        f_result = feistel_function(R, round_keys_reversed[i])
        new_R = xor_bits(L, f_result)
        L, R = new_L, new_R
    
    # 5. Перестановка перед финальной перестановкой
    combined = combine_half(R, L)
    
    # 6. Финальная перестановка IP_INV
    plaintext = permute(combined, IP_INV)
    
    return plaintext


