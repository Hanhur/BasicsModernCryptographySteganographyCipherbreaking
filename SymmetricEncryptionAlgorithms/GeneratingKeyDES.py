# ФУНКЦИИ ДЛЯ РАБОТЫ С СООБЩЕНИЯМИ ПРОИЗВОЛЬНОЙ ДЛИНЫ (PKCS#7 Padding)
# ============================================================================

def pad_message(text):
    """
    Добавляет PKCS#7 паддинг к тексту для выравнивания до 64 бит (8 байт).
    """
    block_size = 8  # 8 байт = 64 бита
    padding_len = block_size - (len(text) % block_size)
    if padding_len == 0:
        padding_len = block_size
    padding = chr(padding_len) * padding_len
    return text + padding

def unpad_message(text):
    """
    Удаляет PKCS#7 паддинг.
    """
    padding_len = ord(text[-1])
    if padding_len > len(text):
        return text
    return text[:-padding_len]

def des_encrypt(plaintext, key):
    """
    Шифрует строку произвольной длины с помощью DES.
    """
    # Преобразуем ключ в битовую строку
    key_bits = string_to_bits(key)
    if len(key_bits) != 64:
        raise ValueError("Ключ должен быть ровно 8 байт (64 бита)")
    
    # Добавляем паддинг к сообщению
    padded_text = pad_message(plaintext)
    
    # Преобразуем в биты
    text_bits = string_to_bits(padded_text)
    
    # Шифруем по блокам (по 64 бита)
    ciphertext_bits = ''
    for i in range(0, len(text_bits), 64):
        block = text_bits[i:i + 64]
        encrypted_block = des_encrypt_block(block, key_bits)
        ciphertext_bits += encrypted_block
    
    return ciphertext_bits

def des_decrypt(ciphertext_bits, key):
    """
    Расшифровывает битовую строку с помощью DES.
    """
    # Преобразуем ключ в битовую строку
    key_bits = string_to_bits(key)
    if len(key_bits) != 64:
        raise ValueError("Ключ должен быть ровно 8 байт (64 бита)")
    
    # Расшифровываем по блокам
    plaintext_bits = ''
    for i in range(0, len(ciphertext_bits), 64):
        block = ciphertext_bits[i:i + 64]
        decrypted_block = des_decrypt_block(block, key_bits)
        plaintext_bits += decrypted_block
    
    # Преобразуем биты в строку
    plaintext = bits_to_string(plaintext_bits)
    
    # Удаляем паддинг
    return unpad_message(plaintext)


